namespace UILib.Layout {
    /**
     * <summary>
     * Types of anchors which can be
     * applied to objects to determine
     * how they are positioned in their parent container.
     * </summary>
     */
    public enum AnchorType {
        TopLeft,
        TopMiddle,
        TopRight,

        MiddleLeft,
        Middle,
        MiddleRight,

        BottomLeft,
        BottomMiddle,
        BottomRight,
    }
}
